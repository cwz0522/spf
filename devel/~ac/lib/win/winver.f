WINAPI: GetVersionExA KERNEL32.DLL

1 CONSTANT VER_PLATFORM_WIN32_WINDOWS
2 CONSTANT VER_PLATFORM_WIN32_NT

0 VALUE WinNT?
0 VALUE WinVer \ Win2000=50, XP=51, Win2003=52, Vista/2008=60, Windows7/2008R2=61

: InitWinNT ( -- )
  148 HERE !
  HERE GetVersionExA 0<>
  HERE 4 CELLS + @ VER_PLATFORM_WIN32_NT = AND TO WinNT?
  HERE CELL+ @ 10 * HERE CELL+ CELL+ @ + TO WinVer
; InitWinNT

